#include "function_pointers.h"
#include "3-calc.h"
#include <stddef.h>

/**
 * op_add - main
 * @a: var
 * @b: var
 * Return: return
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - main
 * @a: var
 * @b: var
 * Return: return
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - main
 * @a: var
 * @b: var
 * Return: return
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - main
 * @a: var
 * @b: var
 * Return: return
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - main
 * @a: var
 * @b: var
 * Return: return
 */
int op_mod(int a, int b)
{
return (a % b);
}
